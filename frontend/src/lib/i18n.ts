// ---------- Dictionary registry ----------
const DICTS: Record<Language, Dict> = { en, hi, mr, gu, ta, te, ml, bn, pa, ur, or };

export function t(key: string, lang: Language = "en", vars?: Record<string, string | number>): string {
  let s = DICTS[lang]?.[key];
  if (s === undefined) s = en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}

// Translate category English key (e.g. "Food") to selected language.
export function tCat(category: string, lang: Language = "en"): string {
  if (!category) return "";
  const key = `cat_${category.toLowerCase().replace(/\s+/g, "_")}`;
  const translated = DICTS[lang]?.[key];
  if (translated) return translated;
  const englishLabel = en[key];
  return englishLabel ?? category;
}

export function tDay(diff: number, lang: Language = "en"): string {
  if (diff === 0) return t("d_today", lang);
  if (diff === 1) return t("d_yesterday", lang);
  return t("d_n_days_ago", lang, { n: diff });
}

// Check if a language is right-to-left
export function isRTL(lang: Language): boolean {
  return lang === "ur";
}
